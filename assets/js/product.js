function renderAllProducts() {
    const displayLang = localStorage.getItem('lang') || 'ar';
    const doors = products.doors || [];
    const stands = products.stands || [];

    const doorTypes = {
        armored_doors: ".armored_doors",
        interior_doors: ".interior_doors",
        pivotal_doors: ".pivotal_doors",
        hidden_doors: ".hidden_doors"
    };

    // عرض الابواب    
    Object.keys(doorTypes).forEach(type => {
        const container = document.querySelector(doorTypes[type]);
        if (!container) return;

        const items = doors.find(door => door.type === type)?.items || [];
        container.innerHTML = ""; // تفريغ الحاوية

        items.slice(0, 4).forEach((product, index) => {
            const title = displayLang === 'ar' ? product.name_ar : product.name_en;

            const col = document.createElement("div");
            col.classList.add("col-6", "col-lg-3", "mb-4");

            col.innerHTML = `
                <div class="product-card text-center p-2 pb-4 border rounded h-100">
                    <img src="${product.image}" alt="${product.alt}" class="product-image img-fluid mb-2" />
                    <h4 class="product-title fs-6">${title}</h4>
                    <a href="product-details.html?id=${index}&type=${type}" class="btn_details p-1 ps-2 pe-2 rounded-2">
                        ${displayLang === 'ar' ? 'تفاصيل المنتج' : 'View Details'}
                    </a>
                </div>
            `;

            container.appendChild(col);
        });
    });

    // عرض الستاندات
    const standsContainer = document.querySelector("#stands");
    if (standsContainer && products.stands?.length) {
        const displayLang = localStorage.getItem('lang') || 'en';
        const stands = products.stands;

        const row = document.createElement("div");
        row.classList.add("row", "mt-4", "w-100");

        stands.forEach(stand => {
            const description = displayLang === 'ar' ? stand.description_ar : stand.description_en;

            const col = document.createElement("div");
            col.classList.add("col-12", "col-md-6", "col-lg-4", "mb-4");

            col.innerHTML = `
            <div class="product-card text-center p-3 border rounded h-100 stands">
                <img src="${stand.image}" alt="${stand.alt}" class="img-fluid mb-3 rounded" />
                <p class="product-description">${description}</p>
            </div>
        `;

            row.appendChild(col);
        });

        standsContainer.appendChild(row);
    }

}

document.addEventListener("DOMContentLoaded", renderAllProducts);




