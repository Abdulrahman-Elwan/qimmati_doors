function renderProductPage() {
    const urlParams = new URLSearchParams(window.location.search);
    const selectedType = urlParams.get("type");
    const displayLang = localStorage.getItem('lang') || 'ar';
    const isStandsOnly = selectedType === 'stands';

    const doorsSection = document.querySelector(".doors-section");
    const otherProductsSection = document.querySelector(".other-products-section");
    const standsSection = document.querySelector(".stands-section");

    // تحكم في إظهار/إخفاء الأقسام حسب النوع
    if (isStandsOnly) {
        if (doorsSection) doorsSection.style.display = 'none';
        if (otherProductsSection) otherProductsSection.style.display = 'none';
        if (standsSection) standsSection.style.display = 'block';
        renderStandsSlider();
        return;
    }

    // لو نوع باب معيّن
    if (doorsSection) doorsSection.style.display = 'block';
    if (otherProductsSection) otherProductsSection.style.display = 'block';
    if (standsSection) standsSection.style.display = 'block';

    renderMainProducts(selectedType);
    renderOtherTypesSlider(selectedType);
    renderStandsSlider();
}

